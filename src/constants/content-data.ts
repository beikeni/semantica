/**
 * Get available levels
 */
export function getLevels(): string[] {
  return Object.keys(CONTENT_DATA).sort();
}

/**
 * Get available stories for a given level
 */
export function getStoriesForLevel(level: string): string[] {
  const levelData = CONTENT_DATA[level];
  return levelData ? Object.keys(levelData).sort() : [];
}

/**
 * Get available chapters for a given level and story
 */
export function getChaptersForStory(level: string, story: string): string[] {
  const levelData = CONTENT_DATA[level];
  if (!levelData) return [];
  return levelData[story] ?? [];
}

